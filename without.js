const without = (source, itemsToRemove) => {
  let newArr = source;
  for (let i = 0; i < source.length; i++) {
    for (let j = 0; j < itemsToRemove.length; j++) {
      if (source[i] === itemsToRemove[j]) {
        newArr.splice(i);
      }
    }
  }
  return newArr;
};

